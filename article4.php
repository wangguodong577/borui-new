		                <div class="the-content-single article">
							<p>海淀法院网10月21日消息，近日，海淀法院一审判决婚趣公司在“婚趣网”网站、“婚趣”微博中使用“婚趣”的行为不侵害蔡先生在第41类、第45类上注册的“婚趣”商标，驳回了蔡先生的全部诉讼请求。</p>
							<p>原告蔡先生在核定服务项目第45类服装出租、晚礼服出租、计划和安排婚礼服务等类别，以及第41类摄影、舞台布景出租等类别上分别注册了“婚趣”商标，享有注册商标专用权。婚趣公司于2014年8月8日被核准企业名称，于9月17日成立，该时间晚于蔡先生第45类“婚趣”商标获得注册的时间，但早于蔡先生申请第41类婚趣商标的注册时间。</p>
							<p>法院认为，蔡先生注册的第45类“婚趣”商标核定服务类别为晚礼服出租、服装出租、计划和安排婚礼服务等，而婚趣公司提供的是婚纱摄影服务，其本质是摄影，与礼服出租、计划和安排婚礼服务在功能、销售渠道、营销重点等方面不同。因此，婚趣公司的行为不侵害蔡先生享有的第45类“婚趣”商标专用权。</p>
							<p>根据法院查明的事实，婚趣公司及其法定代表人已在婚纱摄影等服务上在先使用“婚趣”商标，因此，婚趣公司在第41类“婚趣”商标申请日前已在婚纱摄影领域使用在先。同时，蔡先生所提交的证据无法证明其存在在先使用第41类婚趣商标，也无法证明婚趣公司在明知蔡先生的这些使用行为的情况下依然实施涉案行为而存在主观故意。法院认定，婚趣公司在第41类“婚趣”商标申请注册日后持续在婚纱摄影服务中使用“婚趣”的行为，未侵犯蔡先生注册的第41类“婚趣”商标权。</p>
