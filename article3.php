		                    <h1 class="post-title"><a>金庸诉作家江南侵权索赔500万</a></h1>
		                </div>
		                <div class="clearer"></div>
		                <div class="the-content-single article">
							<p>近日，著名作家金庸在将作家江南告上法庭，起诉江南及北京联合出版公司等三家机构涉嫌著作权侵权，原因是江南早期作品《此间的少年》使用了金庸作品中的乔峰、令狐冲等人物。金庸要求他们“立即停止侵犯著作权的行为，停止复制、发行小说《此间的少年》，封存并销毁库存图书、公开道歉并且赔偿500万元”。</p>
							<p>有业内人士表示，目前网络文学中此类 “同人作品”大量存在而且市场广泛，如果此次判定江南侵犯著作权，可能会影响到此类作品的出版。“同人作品”究竟为何流行？江南使用乔峰、令狐冲等名字重新创作作品，是否真的会侵犯的金庸的著作权？</p>
							<p>在提交给广州市天河区法院的诉状中，金庸要求江南立即停止复制、发行《此间的少年》，封存并销毁库存图书；除了道歉之外，另要赔偿经济损失500万。</p>
							<p>然而，记者今天浏览亚马逊、当当网等，《此间的少年》仍在销售。2011年小说十年纪念版，至今还位于京东商城青春文学畅销榜的第37位。此前还传出华策影业有意把小说改编成电影的消息。</p>
							<p>其实早在2002年《此间的少年》就已风靡网络。小说讲了宋代“汴京大学”里乔峰、郭靖、令狐冲等人的故事，不过上演的却是一出青春校园爱情轻喜剧。作者江南也曾公开承认，书中人物姓名均来自金庸作品。接到金庸的诉讼消息后，23号晚间江南发出声明，首先解释当年的创作动机——“最初网络连载，主要是出于好玩的心理”。同时表示，会在诉讼期暂停与该书相关的所有项目开发。江南强调，他尊敬金庸先生个人和作品，并没有侵权的想法；无论诉讼结果如何，他对自己22岁那年的唐突都感到抱歉。</p>
							<p>网络小说作家筠心解释说，把已存在的漫画、小说、影视作品里的人物，放入新的故事环境，加入作者本人的想法，表达新的主题的作品，又被称为“同人文”。江南《此间的少年》就属于“同人小说”的范畴。</p>
							<p>筠心说：“一旦一个作品火了，基于对里面人物的喜爱，读者会根据自己对人物不同气质的理解，类似写一个番外篇一样。有的是为了满足比如故事原先是悲剧结局他在编一个喜剧；有的是本来两个人物是敌对状态，但是被人改成了情侣。”</p>
							<p>某知名网络文学平台经理张伟甍表示，大部分“同人文”借用的都是较为知名作品的人物，利用原作粉丝圈的身份认同感，定向吸引了一大批愿意付费阅读的读者。同人现在有固定的读者群体，比如晋江、岳麓，都是比较知名的两个写同人作品的。读者群体很集中，如果你能把这批人都吸引过来的话，他是有很大的付费能力的，因为他就喜欢看这些人物。</p>
							<p>长期以来，“同人作品”都处于监管的灰色地带。金庸此前公开表态说，“文学一定要原创，部分网民拿我小说的人物去发展自己的小说，是完全不可以的。在香港用我小说人物的名字是要付钱的。”周星驰在电影《功夫》中用了杨过、小龙女等，每用一次就付钱1万元。</p>
							<p>不过，江南表示，十几年前出版《此间》时也曾因书中人名而惴惴不安，但咨询法律人士后被告知这种形式在当时未曾触及相关的法律规定。</p>
							<p>曾经代理作家联盟诉百度文库案的律师王国华认为：著作权侵权的前提是，非法使用或者未经许可使用他人的作品。如果只用到人物的名字，肯定不属于著作权侵权；如果仅仅性格有一定相似性，比如金庸天龙八部中的乔峰，社会上也会有很多人性格类似，也不属于著作权法范畴。关键看两部作品在故事情节、内容、语句表达方面是否有相似的地方。</p>
							<p>翻看部分网文平台，同人文学种类繁多，主角从柯南流川枫到当红影视作品眼花缭乱。长江中文网总编辑董江波坦言，由于“同人文”一般只与原著人物名字或者背景体系相同，具体情节并无相似，因此很少有原著作者追究。</p>
							<p>尽管如此，部分网文平台在选择上架作品时，仍会忌惮同人小说存在的法律风险。张伟甍介绍说：“网文圈子还有一个“避风港”原则。就是在你还没有发现之前，我第一次发现了，我告诉你，你删除了，只要不是有太大影响力都没有问题。但是江南写的书大家也都知道，他本身就是个名人。同时他又联合和出版社出版了，导致问题扩大化。相当于借着金庸的名气、人物来扩大影响力，其实大家在收同人作品的时候，很多网站都是不愿意收。因为很容易被管控、存在一定的风险。”</p>
							<p>加上近年来IP概念日盛，一个文学作品能复合衍生出影视剧、游戏、各类商品，随之产生的收益甚至远大于作品本身，因此引发与原作者纠纷的可能性越来越大。不少网文平台甚至调整了与新近作者签约的方式。</p>
							<p>筠心说：“现在的网文签约都会一开始就跟你签订说，你这部小说出现的人物，我也给你买断了。你这些人物以后衍生出来的任何花样繁出的同人文和同人作品，包括造型、动漫游戏，哪怕实物玩偶，版权都要有明确归属。</p>
